def require_regular_or_absent(path: Path) -> bool:
    try:
        st = os.lstat(path)
    except FileNotFoundError:
        return False
    except OSError as exc:
        raise TaskError(f"{path}: cannot stat safely before open: {exc}") from exc
    if path.is_symlink() or not stat.S_ISREG(st.st_mode):
        raise TaskError(f"{path}: refusing to open non-regular file")
    return True


def require_regular_file(path: Path) -> None:
    if not require_regular_or_absent(path):
        raise TaskError(f"{path}: missing required regular file")


class FileLock:
    def __init__(self, path: Path):
        self.path = path
        self._fh = None

    def __enter__(self) -> "FileLock":
        self.path.parent.mkdir(parents=True, exist_ok=True)
        require_regular_or_absent(self.path)
        self._fh = self.path.open("a+", encoding="utf-8")
        fcntl.flock(self._fh, fcntl.LOCK_EX)
        return self

    def __exit__(self, exc_type, exc, tb) -> None:
        assert self._fh is not None
        fcntl.flock(self._fh, fcntl.LOCK_UN)
        self._fh.close()


def utc_now() -> str:
    return dt.datetime.now(dt.timezone.utc).isoformat(timespec="seconds")


def _resolve_loose(path: Path) -> Path:
    return path.expanduser().resolve(strict=False)


def _strip_managed_worktree(path: Path) -> Path:
    resolved = _resolve_loose(path)
    parts = resolved.parts
    for index, part in enumerate(parts[:-2]):
        if part == ".claude" and parts[index + 1] == "worktrees":
            return _resolve_loose(Path(*parts[:index]))
    return resolved


def resolve_project_root(value: str | None = None) -> Path:
    explicit = value or os.environ.get("GOALFLIGHT_PROJECT_ROOT")
    if explicit:
        return _strip_managed_worktree(Path(explicit))

    cwd = Path.cwd()
    try:
        top = Path(
            subprocess.check_output(
                ["git", "rev-parse", "--show-toplevel"],
                cwd=str(cwd),
                text=True,
                stderr=subprocess.DEVNULL,
            ).strip()
        )
        common_raw = subprocess.check_output(
            ["git", "rev-parse", "--git-common-dir"],
            cwd=str(cwd),
            text=True,
            stderr=subprocess.DEVNULL,
        ).strip()
    except (OSError, subprocess.CalledProcessError):
        return _strip_managed_worktree(cwd)

    common = Path(common_raw)
    if not common.is_absolute():
        common = top / common
    if common.name == ".git":
        return _strip_managed_worktree(common.parent)
    return _strip_managed_worktree(top)


