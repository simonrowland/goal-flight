def _source_ref_path(source_ref: object) -> str:
    text = str(source_ref or "")
    match = re.match(r"^(.+):\d+$", text)
    return match.group(1) if match else text


def _manual_harvest_drafts(payload: dict[str, Any]) -> list[dict[str, Any]]:
    return [
        draft
        for draft in LIST_TYPE(payload.get("drafts") or [])
        if draft.get("harvest_source") == "manual-source"
    ]


def _source_match_text_lines(source_matches: list[dict[str, Any]]) -> list[str]:
    lines: list[str] = []
    for summary in source_matches:
        pattern = str(summary.get("pattern") or "")
        if int(summary.get("matches") or 0) == 0:
            lines.append(f"0 matches for {pattern}")
        if summary.get("file_as_task"):
            lines.append(
                f"file-as-task for {pattern}: {int(summary.get('files_matched') or 0)} matched, "
                f"{int(summary.get('imported') or 0)} imported, "
                f"{int(summary.get('closed_skipped') or 0)} closed_skipped, "
                f"{int(summary.get('no_header_skipped') or 0)} no_header_skipped"
            )
        skipped = LIST_TYPE(summary.get("skipped_sources") or [])
        if skipped:
            preview = ", ".join(
                f"{item.get('path')} ({item.get('reason')})"
                for item in skipped[:5]
                if isinstance(item, dict)
            )
            more = f", +{len(skipped) - 5} more" if len(skipped) > 5 else ""
            lines.append(f"skipped {len(skipped)} managed source(s) for {pattern}: {preview}{more}")
        dropped = int(summary.get("dropped") or 0)
        if dropped:
            limit = int(summary.get("source_limit") or DEFAULT_SOURCE_LIMIT)
            lines.append(
                f"dropped {dropped} matched file(s) for {pattern} after --source-limit {limit}; "
                "use --source-limit N to include more"
            )
    return lines


def _source_match_summary_suffix(source_matches: list[dict[str, Any]]) -> str:
    notes = _source_match_text_lines(source_matches)
    return "" if not notes else "; " + "; ".join(notes)


def _print_migrate_preview(payload: dict[str, Any]) -> None:
    drafts = _manual_harvest_drafts(payload)
    by_source: dict[str, list[str]] = {}
    for draft in drafts:
        source = _source_ref_path(draft.get("source_ref")) or "(unknown source)"
        by_source.setdefault(source, []).append(str(draft.get("title") or "(untitled)"))
    print(f"PREVIEW: {len(drafts)} source candidate(s)")
    for source in sorted(by_source):
        titles = by_source[source]
        print(f"- {source}: {len(titles)} candidate(s)")
        for title in titles[:10]:
            print(f"  - {title}")
        if len(titles) > 10:
            print(f"  - ... {len(titles) - 10} more")
    seen_sources = set(by_source)
    for summary in LIST_TYPE(payload.get("source_matches") or []):
        pattern = str(summary.get("pattern") or "")
        if int(summary.get("matches") or 0) == 0:
            print(f"- {pattern}: 0 matched file(s)")
        elif int(summary.get("candidates") or 0) == 0 and pattern not in seen_sources:
            print(f"- {pattern}: 0 candidate(s)")
        for line in _source_match_text_lines([summary]):
            if line.startswith("0 matches for "):
                continue
            print(f"- {line}")


def _cmd_migrate(store: TaskStore, args: argparse.Namespace) -> int:
    if not args.sources:
        raise TaskError("migrate requires at least one --source <glob>")
    if getattr(args, "dry_run", False) and args.apply:
        raise TaskError("migrate --dry-run cannot be combined with --apply")
    preview = _run_harvest_json(store, args, dry_run=True)
    if getattr(args, "json", False) and not args.apply:
        print(json.dumps(preview, ensure_ascii=False, sort_keys=True))
        return 0
    if getattr(args, "json", False) and args.apply:
        result = _run_harvest_json(store, args, dry_run=False)
        print(json.dumps({"apply": result, "preview": preview}, ensure_ascii=False, sort_keys=True))
        return 0
    _print_migrate_preview(preview)
    if not args.apply:
        print("NO CHANGES: re-run with --apply to create harvest draft items")
        print("NEXT: curate drafts with python3 goalflight_task.py list --tag harvest")
        return 0
    result = _run_harvest_json(store, args, dry_run=False)
    print(f"APPLIED: harvest created {len(result.get('created') or [])} draft item(s) ({result.get('skipped', 0)} skipped)")
    print("NEXT: list drafts with python3 goalflight_task.py list --tag harvest")
    print("NEXT: curate with python3 goalflight_task.py done <id>, accept <id>, or lane <id> <lane>")
    return 0


def _cmd_accept(store: TaskStore, args: argparse.Namespace) -> int:
    actor = _actor(args)

    def update(items: list[dict[str, Any]]) -> None:
        rows = store.derived_rows_for_items(items)
        by_id = {row["id"]: row for row in rows}
        row = by_id.get(args.item_id)
        if row is None:
            raise TaskError(f"{store.tasks_path}: item not found: {args.item_id}")
        if _is_done_reviewed(row):
            raise TaskError(f"{args.item_id}: already done-reviewed")
        if row.get("derived_status") != "awaiting-review":
            raise TaskError(
                f"{args.item_id}: not DONE/awaiting-review; "
                f"run python3 goalflight_task.py done {args.item_id} or wait for worker completion first"
            )
        review = _latest_review_breadcrumb(row)
        if not review:
            raise TaskError(
                f"{args.item_id}: no logged review; "
                f"run python3 goalflight_task.py review {args.item_id} --verdict clean --dispatch <id> before accept"
            )
        if review.get("verdict") != "clean":
            raise TaskError(f"{args.item_id}: latest review verdict is {review.get('verdict')!r}, not clean")

        item = next(item for item in items if item.get("id") == args.item_id)
        item["done"] = True
        item["done_reviewed"] = True
        item["done_reviewed_at"] = utc_now()
        item["done_reviewed_by"] = actor
        item["closed_at"] = item["done_reviewed_at"]
        item["closed_by"] = actor
        item["accepted_review_dispatch_id"] = review.get("dispatch_id")
        if review.get("findings_ref"):
            item["accepted_review_findings_ref"] = review.get("findings_ref")
        _append_audit(item, "accept", actor, review_dispatch_id=review.get("dispatch_id"))

    store.mutate_items(update, allow_invalid_live_mirror=True)
    print(args.item_id)
    return 0


