def _canonical_item_id(value: str, lookup: dict[str, str]) -> str:
    return lookup.get(value.lower(), value)


def _ticket_link(item_id: str) -> str:
    return f"[{_md_text(item_id)}](ticket.html?id={quote(item_id, safe='')})"


def _md_autolink_ids(value: Any, lookup: dict[str, str]) -> str:
    text = _md_text(value)
    return ITEM_ID_RE.sub(lambda match: _ticket_link(_canonical_item_id(match.group(1), lookup)), text)


def _md_value_link(value: Any, lookup: dict[str, str]) -> str:
    text = str(value if value is not None else "")
    if ITEM_ID_RE.fullmatch(text):
        return _ticket_link(_canonical_item_id(text, lookup))
    return _md_autolink_ids(text, lookup)


def _md_list(values: Any, lookup: dict[str, str]) -> str:
    if not isinstance(values, LIST_TYPE) or not values:
        return ""
    return ", ".join(_md_value_link(value, lookup) for value in values if value not in (None, ""))


def _dedupe_strs(values: list[Any] | tuple[Any, ...] | None) -> list[str]:
    out: list[str] = []
    for value in values or []:
        if value in (None, ""):
            continue
        text = str(value)
        if text not in out:
            out.append(text)
    return out


def _clean_markdown_title(value: str) -> str:
    text = value.strip()
    text = re.sub(r"^\[[ xX]\]\s+", "", text)
    text = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", text)
    text = text.replace("`", "")
    text = text.replace("**", "").replace("__", "")
    text = re.sub(r"\s+", " ", text)
    return text.strip(" -\t")


def _strip_markdown_wrappers(value: str) -> str:
    text = value.strip()
    pairs = ("**", "__", "~~", "`", "*", "_")
    while text:
        for marker in pairs:
            if len(text) > len(marker) * 2 and text.startswith(marker) and text.endswith(marker):
                text = text[len(marker) : -len(marker)].strip()
                break
        else:
            return text
    return text


def _harvest_title_key(value: Any) -> str:
    text = str(value or "").strip()
    text = re.sub(r"^#{1,6}\s+", "", text)
    text = re.sub(r"^\[[ xX]\]\s+", "", text)
    text = re.sub(r"\[([^\]]+)\]\([^)]+\)", r"\1", text)
    text = html.unescape(_strip_markdown_wrappers(text))
    return re.sub(r"\s+", " ", text).strip().lower()


def _placeholder_key(value: Any) -> str:
    text = html.unescape(str(value or ""))
    text = text.replace("`", "")
    return re.sub(r"\s+", " ", text).strip().lower()


def _harvest_has_placeholder(value: Any) -> bool:
    text = _placeholder_key(value)
    if not text:
        return False
    for phrase in HARVEST_PLACEHOLDER_PHRASES:
        if _placeholder_key(phrase) in text:
            return True
    for token in HARVEST_PLACEHOLDER_TOKENS:
        if _placeholder_key(token) in text:
            return True
    return False


