def _harvest_heading_prompt(lines: list[str]) -> str | None:
    text = "\n".join(lines).strip()
    if not text:
        return None
    if len(text) <= HARVEST_HEADING_PROMPT_LIMIT:
        return text
    truncated = text[:HARVEST_HEADING_PROMPT_LIMIT].rstrip()
    return f"{truncated}\n\n[truncated after first {HARVEST_HEADING_PROMPT_LIMIT} chars from section body]"


def _harvest_first_h1(lines: list[str]) -> tuple[int, str] | None:
    for lineno, line in enumerate(lines, start=1):
        match = re.match(r"^#(?!#)\s+(.+?)\s*#*\s*$", line)
        if match:
            return lineno, match.group(1)
    return None


def _harvest_file_task_title(value: str) -> str:
    text = _clean_markdown_title(value)
    # Label id after a bare space must contain a digit ("TASK 12:", "Work-package F1:")
    # so prose like "Task force: Improve triage" is never stripped; explicitly
    # joined ids ("TASK-ALPHA:", "TASK #12:") may be any token.
    match = re.match(
        r"^(?:task|work[\s-]?package)"
        r"(?:\s*[-#]\s*[A-Za-z0-9_.-]+|\s+[A-Za-z0-9_.-]*\d[A-Za-z0-9_.-]*)?"
        r"\s*(?::|[-\u2013\u2014])\s*(.+)$",
        text,
        flags=re.IGNORECASE,
    )
    return _clean_markdown_title(match.group(1)) if match else text


def _harvest_file_metadata_value(lines: list[str], key: str) -> str | None:
    field = re.escape(key)
    pattern = re.compile(
        rf"^\s*(?:[-*]\s*)?(?:\*\*)?\s*{field}\s*(?::\s*(?:\*\*)?|\*\*\s*:)\s*(.+?)\s*$",
        re.IGNORECASE,
    )
    for line in lines:
        match = pattern.match(line)
        if match:
            return _strip_markdown_wrappers(match.group(1).strip())
    return None


def _harvest_file_status_token(value: str | None) -> str | None:
    if value is None:
        return None
    text = _clean_markdown_title(value)
    if re.match(r"^in[\s_-]+progress\b", text, flags=re.IGNORECASE):
        return "IN_PROGRESS"
    match = re.match(r"^([A-Za-z]+(?:_[A-Za-z]+)*)\b", text)
    if not match:
        return None
    return match.group(1).upper().replace("-", "_")


def _harvest_priority_tag(value: str | None) -> str | None:
    if value is None:
        return None
    text = _clean_markdown_title(value).lower()
    text = re.sub(r"\s+", "-", text).strip("-")
    return f"priority:{text}" if text else None


def _harvest_list_item_at(lines: list[str], index: int) -> tuple[str, int] | None:
    match = re.match(r"^\s*(?:[-*]\s+|\d+[.)]\s+)(?:\[[ xX]\]\s+)?(.+?)\s*$", lines[index])
    if not match:
        return None
    parts = [match.group(1).strip()]
    next_index = index + 1
    while next_index < len(lines):
        line = lines[next_index]
        if not line.strip():
            break
        if re.match(r"^\s*(?:[-*]\s+|\d+[.)]\s+)(?:\[[ xX]\]\s+)?", line):
            break
        if re.match(r"^#{1,6}\s+", line):
            break
        if re.match(r"^\s*\|", line):
            break
        if line.startswith("\t") or re.match(r"^\s{2,}\S", line):
            parts.append(line.strip())
            next_index += 1
            continue
        break
    return " ".join(parts), next_index


def _split_markdown_table_row(line: str) -> list[str]:
    text = line.strip()
    if not text.startswith("|"):
        return []
    text = text[1:]
    if text.endswith("|"):
        text = text[:-1]
    return [cell.replace(r"\|", "|").strip() for cell in re.split(r"(?<!\\)\|", text)]


def _is_markdown_table_separator(cells: list[str]) -> bool:
    if not cells:
        return False
    for cell in cells:
        marker = cell.strip().replace(" ", "")
        if not re.fullmatch(r":?-{3,}:?", marker):
            return False
    return True


def _harvest_table_title_indexes(headers: list[str]) -> list[int]:
    indexes: list[int] = []
    for index, header in enumerate(headers):
        if HARVEST_TABLE_TITLE_HEADER_RE.search(_clean_markdown_title(header)):
            indexes.append(index)
    return indexes


def _harvest_table_state_indexes(headers: list[str]) -> list[int]:
    indexes: list[int] = []
    for index, header in enumerate(headers):
        if _harvest_title_key(header) in HARVEST_TABLE_STATE_HEADERS:
            indexes.append(index)
    return indexes


def _harvest_table_row_closed(cells: list[str], state_indexes: list[int]) -> bool:
    if not state_indexes:
        return False
    state_cells = [cells[index] for index in state_indexes if index < len(cells)]
    haystack = " ".join(state_cells)
    return HARVEST_TABLE_CLOSED_RE.search(haystack) is not None


def _harvest_table_row_open(cells: list[str], state_indexes: list[int]) -> bool:
    if not state_indexes:
        return False
    state_cells = [cells[index] for index in state_indexes if index < len(cells)]
    haystack = " ".join(state_cells)
    return HARVEST_TABLE_OPEN_RE.search(haystack) is not None


def _harvest_table_row_title(cells: list[str], state_indexes: list[int], title_indexes: list[int]) -> str:
    for index in title_indexes:
        if index < len(cells):
            title = _harvest_position_title(cells[index])
            if title:
                return title
    for index, cell in enumerate(cells):
        if index in state_indexes:
            continue
        title = _harvest_position_title(cell)
        if title:
            return title
    return ""


def _harvest_table_at(
    store: TaskStore,
    path: Path,
    lines: list[str],
    index: int,
    *,
    default_kind: str,
    source_type: str,
    lane: str | None,
    active_task_context: bool,
) -> tuple[list[dict[str, Any]], int] | None:
    header = _split_markdown_table_row(lines[index])
    if not header or index + 1 >= len(lines):
        return None
    separator = _split_markdown_table_row(lines[index + 1])
    if not _is_markdown_table_separator(separator):
        return None

    next_index = index + 2
    rows: list[tuple[int, list[str]]] = []
    while next_index < len(lines):
        cells = _split_markdown_table_row(lines[next_index])
        if not cells:
            break
        rows.append((next_index + 1, cells))
        next_index += 1

    state_indexes = _harvest_table_state_indexes(header)
    title_indexes = _harvest_table_title_indexes(header)
    table_is_task_shaped = bool(state_indexes or title_indexes)
    if not table_is_task_shaped or (not active_task_context and not state_indexes):
        return [], next_index

    candidates: list[dict[str, Any]] = []
    for row_lineno, cells in rows:
        if state_indexes:
            if _harvest_table_row_closed(cells, state_indexes):
                continue
            if not _harvest_table_row_open(cells, state_indexes):
                continue
        title = _harvest_table_row_title(cells, state_indexes, title_indexes)
        if not title or _harvest_has_placeholder(title):
            continue
        candidate = _harvest_candidate(
            store,
            kind=default_kind,
            title=title,
            source_path=path,
            source_type=source_type,
            line=row_lineno,
            position_key=f"table:{index + 1}:{row_lineno}",
        )
        if candidate is not None:
            if lane is not None:
                candidate["lane"] = lane
            candidate["tags"] = [*LIST_TYPE(candidate.get("tags") or []), "table-row"]
            candidates.append(candidate)
    return candidates, next_index


