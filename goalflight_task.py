def _json_for_script(value: Any) -> str:
    payload = json.dumps(value, ensure_ascii=False, indent=2)
    return (
        payload.replace("&", "\\u0026")
        .replace("<", "\\u003c")
        .replace(">", "\\u003e")
        .replace("\u2028", "\\u2028")
        .replace("\u2029", "\\u2029")
    )


def _md_text(value: Any) -> str:
    text = str(value if value is not None else "")
