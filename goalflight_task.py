def _breadcrumb_from_record(record: dict[str, Any]) -> dict[str, Any]:
    state = (
        _dispatch_status_from_record(record, live=True)
        or _dispatch_status_from_record(record, live=False)
        or record.get("state")
    )
    ts = record.get("ended_at") or record.get("updated_at") or record.get("started_at") or utc_now()
    crumb: dict[str, Any] = {
        "dispatch_id": record.get("dispatch_id"),
        "agent": record.get("agent"),
        "log": record.get("stdout_path") or record.get("log"),
        "stdout_path": record.get("stdout_path"),
        "stderr_path": record.get("stderr_path"),
        "status_path": record.get("status_path"),
        "started_at": record.get("started_at"),
        "ended_at": record.get("ended_at"),
        "state": state,
        "ts": ts,
        "terminal_state": _record_terminal_state(record),
        "marker": record.get("marker"),
        "worker_pid": record.get("worker_pid"),
        "hostname": record.get("hostname"),
        "last_worker_state": record.get("last_worker_state") or _status_snapshot_from_record(record),
    }
    return {k: v for k, v in crumb.items() if v not in (None, "", [], {})}


def _breadcrumb_key(crumb: dict[str, Any]) -> str:
    marker = crumb.get("marker")
    marker_key = _canonical_json(marker) if isinstance(marker, dict) else str(marker or "")
    return "|".join(
        [
            str(crumb.get("dispatch_id") or ""),
            str(crumb.get("state") or ""),
            str(crumb.get("ts") or ""),
            marker_key,
        ]
    )


def _latest_dispatch_breadcrumb(item: dict[str, Any]) -> dict[str, Any] | None:
    dispatches = item.get("dispatches")
    if not isinstance(dispatches, LIST_TYPE):
        return None
    candidates = []
    for crumb in dispatches:
        if not isinstance(crumb, dict):
            continue
        if _dispatch_status_from_record(crumb, live=False):
            candidates.append(crumb)
    if not candidates:
        return None
    return sorted(candidates, key=_record_sort_time)[-1]


def _latest_breadcrumb(item: dict[str, Any], *, role: str | None = None) -> dict[str, Any] | None:
    dispatches = item.get("dispatches")
    if not isinstance(dispatches, LIST_TYPE):
        return None
    candidates = []
    for crumb in dispatches:
        if not isinstance(crumb, dict):
            continue
        if role is not None and crumb.get("role") != role:
            continue
        candidates.append(crumb)
    if not candidates:
        return None
    return sorted(candidates, key=_record_sort_time)[-1]


def _latest_review_breadcrumb(item: dict[str, Any]) -> dict[str, Any] | None:
    return _latest_breadcrumb(item, role="review")


def _iso_from_epoch(epoch: int) -> str | None:
    if epoch <= 0:
        return None
    return dt.datetime.fromtimestamp(epoch, tz=dt.timezone.utc).isoformat(timespec="seconds")


def _epoch_from_time(value: Any) -> int:
    parsed = _parse_time(value)
    if parsed == dt.datetime.min.replace(tzinfo=dt.timezone.utc):
        return 0
    return int(parsed.timestamp())


def _parse_since(value: str | None) -> int | None:
    if value in (None, ""):
        return None
    raw = str(value).strip()
    now = int(dt.datetime.now(dt.timezone.utc).timestamp())
    match = re.fullmatch(r"(\d+)([smhdw])", raw)
    if match:
        amount = int(match.group(1))
        multiplier = {"s": 1, "m": 60, "h": 3600, "d": 86400, "w": 604800}[match.group(2)]
        return now - amount * multiplier
    match = re.fullmatch(r"now([+-])(\d+)", raw)
    if match:
        delta = int(match.group(2))
        return now - delta if match.group(1) == "-" else now + delta
    if raw.isdigit():
        return int(raw)
    parsed = _parse_time(raw)
    if parsed == dt.datetime.min.replace(tzinfo=dt.timezone.utc):
        raise TaskError(f"invalid --since value {value!r}; use 1h, now-3600, epoch seconds, or an ISO timestamp")
    return int(parsed.timestamp())


def _item_query_epoch(item: dict[str, Any]) -> int:
    epochs = [_epoch_from_time(item.get("created_at"))]
    crumb = _latest_breadcrumb(item)
    if crumb:
        epochs.append(_epoch_from_time(crumb.get("ts") or crumb.get("ended_at") or crumb.get("updated_at") or crumb.get("started_at")))
    return max(epochs) if epochs else 0


def _item_dispatch_epoch(item: dict[str, Any]) -> int:
    crumb = _latest_dispatch_breadcrumb(item)
    if not crumb:
        return 0
    return _epoch_from_time(crumb.get("ts") or crumb.get("ended_at") or crumb.get("updated_at") or crumb.get("started_at"))


def _is_done_reviewed(item: dict[str, Any]) -> bool:
    if item.get("done_reviewed") is True:
        return True
    return item.get("kind") == "decision" and item.get("done") is True


def _matches_canned_status(row: dict[str, Any], status: str | None) -> bool:
    if not status:
        return True
    if status == "outstanding":
        return not _is_done_reviewed(row)
    if status == "awaiting-review":
        return not _is_done_reviewed(row) and row.get("derived_status") == "awaiting-review"
    if status == "working":
        return not _is_done_reviewed(row) and row.get("derived_status") == "working"
    if status == "decision":
        return not _is_done_reviewed(row) and row.get("derived_status") == "decision"
    if status == "waiting":
        return not _is_done_reviewed(row) and row.get("derived_status") == "waiting"
    if status == "delegated":
        return _latest_dispatch_breadcrumb(row) is not None
    if status == "done-reviewed":
        return _is_done_reviewed(row)
    raise TaskError(f"unknown list status {status!r}; expected one of {', '.join(sorted(CANNED_LIST_STATUSES))}")


def _coerce_query(query: Any = None, **overrides: Any) -> dict[str, Any]:
    if query is None:
        out: dict[str, Any] = {}
    elif isinstance(query, str):
        out = {"status": query}
    elif isinstance(query, dict):
        out = dict(query)
    else:
        raise TaskError("query must be None, a canned status string, or a dict")
    out.update({k: v for k, v in overrides.items() if v not in (None, [], {})})
    blocked_by = out.get("blocked_by")
    if isinstance(blocked_by, str):
        out["blocked_by"] = _split_csv([blocked_by])
    elif isinstance(blocked_by, LIST_TYPE):
        values: list[str] = []
        for value in blocked_by:
            if isinstance(value, str):
                values.extend(_split_csv([value]))
        out["blocked_by"] = values
    status = out.get("status")
    if status not in (None, "") and status not in CANNED_LIST_STATUSES:
        raise TaskError(f"unknown list status {status!r}; expected one of {', '.join(sorted(CANNED_LIST_STATUSES))}")
    if out.get("since") not in (None, ""):
        out["since_epoch"] = _parse_since(str(out["since"]))
    return out


class TaskStore:
    def __init__(self, project_root: Path):
        self.project_root = _strip_managed_worktree(project_root)
        self.docs_dir = self.project_root / "docs-private"
        self.tasks_path = self.docs_dir / "tasks.jsonl"
        self.data_js_path = self.docs_dir / "tasks-data.js"
        self.task_decomposition_path = self.docs_dir / "task-decomposition.md"
        self.tasks_done_path = self.docs_dir / "tasks-done.md"
        self.bug_backlog_path = self.docs_dir / "bug-backlog.md"
        self.bugs_done_path = self.docs_dir / "bugs-done.md"
        self.store_lock_path = self.docs_dir / "tasks.lock"
        self.seq_path = self.docs_dir / ".task-seq"
        self.seq_lock_path = self.docs_dir / ".task-seq.lock"
        self.publish_marker_path = self.docs_dir / ".tasks-publish-incomplete.json"
        self.log_dir = self.docs_dir / "log"

    @contextlib.contextmanager
    def store_lock(self):
        self.docs_dir.mkdir(parents=True, exist_ok=True)
        with FileLock(self.store_lock_path):
            yield

    def load_items(self, *, recover_publish: bool = True) -> list[dict[str, Any]]:
        if recover_publish:
            self._recover_interrupted_publish()
        items, _ = _parse_jsonl(self.tasks_path)
        return items

    def items_by_id(self) -> dict[str, dict[str, Any]]:
        return {item["id"]: item for item in self.load_items()}

    def reserve_id(self, family: str) -> str:
        if family not in VALID_FAMILIES:
            raise TaskError(f"unknown id family {family!r}; expected one of {', '.join(sorted(VALID_FAMILIES))}")
        self.docs_dir.mkdir(parents=True, exist_ok=True)
        with FileLock(self.seq_lock_path):
            seq = self._read_seq()
            current = max(int(seq.get(family, 0)), self._max_existing_sequence(family))
            next_value = current + 1
            seq[family] = next_value
            tmp = self.seq_path.with_suffix(".tmp")
            tmp.write_text(json.dumps(seq, indent=2, sort_keys=True) + "\n", encoding="utf-8")
            tmp.replace(self.seq_path)
        return f"ADR-{next_value:03d}" if family == "ADR" else f"{family}-{next_value:03d}"

    def _max_existing_sequence(self, family: str) -> int:
        prefix = "ADR-" if family == "ADR" else f"{family}-"
        max_seen = 0
        for item in self.load_items():
            item_id = item.get("id")
            if not isinstance(item_id, str) or not item_id.startswith(prefix):
                continue
            suffix = item_id.removeprefix(prefix)
            if suffix.isdigit():
                max_seen = max(max_seen, int(suffix))
        return max_seen

    def _read_seq(self) -> dict[str, int]:
        if not require_regular_or_absent(self.seq_path):
            return {}
        text = self.seq_path.read_text(encoding="utf-8").strip()
        if not text:
            return {}
        if text.isdigit():
            return {"t": int(text)}
        try:
            raw = json.loads(text)
        except json.JSONDecodeError as exc:
            raise TaskError(f"{self.seq_path}:1: invalid JSON: {exc.msg}") from exc
        if not isinstance(raw, dict):
            raise TaskError(f"{self.seq_path}:1: expected a JSON object")
        out: dict[str, int] = {}
        for key, value in raw.items():
            if key not in VALID_FAMILIES:
                raise TaskError(f"{self.seq_path}:1: unknown id family {key!r}")
            if not isinstance(value, int) or value < 0:
                raise TaskError(f"{self.seq_path}:1: family {key!r} must be a non-negative integer")
            out[key] = value
        return out

    def mutate_items(self, update: Callable[[list[dict[str, Any]]], Any], *, allow_invalid_live_mirror: bool = False) -> Any:
        with self.store_lock():
            self._recover_interrupted_publish_locked()
            self._snapshot_last_good(require_valid=not allow_invalid_live_mirror)
            items = self.load_items(recover_publish=False)
            result = update(items)
            self.save_items_atomic(items)
            return result

    def save_items_atomic(self, items: list[dict[str, Any]]) -> None:
        self.docs_dir.mkdir(parents=True, exist_ok=True)
        _validate_items_for_write(items)
        staging = Path(tempfile.mkdtemp(prefix=".tasks-stage-", dir=str(self.docs_dir)))
        try:
            self._write_staged_generation(staging, items)
            _run_checker(staging)
            targets = {
                self.tasks_path: staging / "tasks.jsonl",
                self.data_js_path: staging / "tasks-data.js",
                self.task_decomposition_path: staging / "task-decomposition.md",
                self.tasks_done_path: staging / "tasks-done.md",
                self.bug_backlog_path: staging / "bug-backlog.md",
                self.bugs_done_path: staging / "bugs-done.md",
            }
            old_bytes = {path: path.read_bytes() if require_regular_or_absent(path) else None for path in targets}
            generation = hashlib.sha256((staging / "tasks.jsonl").read_bytes()).hexdigest()
            self._write_publish_marker(generation)
            try:
                for target, source in targets.items():
                    source.replace(target)
                _fsync_dir(self.docs_dir)
            except OSError:
                for target, data in old_bytes.items():
                    self._restore_bytes(target, data)
                self._clear_publish_marker()
                raise
            self._clear_publish_marker()
        finally:
            shutil.rmtree(staging, ignore_errors=True)

    @staticmethod
    def _restore_bytes(path: Path, data: bytes | None) -> None:
        if data is None:
            with contextlib.suppress(FileNotFoundError):
                path.unlink()
            _fsync_dir(path.parent)
            return
        tmp = path.with_suffix(path.suffix + ".restore")
        _write_bytes_fsync(tmp, data)
        tmp.replace(path)
        _fsync_dir(path.parent)

    def _write_staged_generation(self, staging: Path, items: list[dict[str, Any]]) -> None:
        _write_text_fsync(staging / "tasks.jsonl", _items_jsonl(items))
        _write_text_fsync(staging / "tasks-data.js", _items_data_js(self._mirror_items_for_script(items)))
        for name, content in self.generated_markdown(items).items():
            _write_text_fsync(staging / name, content)
        _fsync_dir(staging)

    def _mirror_items_for_script(self, items: list[dict[str, Any]]) -> list[dict[str, Any]]:
        rows = self.derived_rows_for_items(items)
        rows_by_id = {row["id"]: row for row in rows}
        mirrored: list[dict[str, Any]] = []
        for item in items:
            out = dict(item)
            derived = rows_by_id.get(item["id"], {}).get("derived_status")
            if isinstance(derived, str) and derived:
                out["derived_status"] = derived
            mirrored.append(out)
        return mirrored

    def _write_publish_marker(self, generation: str) -> None:
        marker = {
            "schema": "goalflight.tasks.publish.v1",
            "generation": generation,
            "canonical": "tasks.jsonl",
            "artifacts": [
                "tasks.jsonl",
                "tasks-data.js",
                "task-decomposition.md",
                "tasks-done.md",
                "bug-backlog.md",
                "bugs-done.md",
            ],
        }
        _write_text_fsync(self.publish_marker_path, json.dumps(marker, ensure_ascii=False, sort_keys=True) + "\n")
        _fsync_dir(self.docs_dir)

    def _clear_publish_marker(self) -> None:
        with contextlib.suppress(FileNotFoundError):
            self.publish_marker_path.unlink()
        _fsync_dir(self.docs_dir)

    def _recover_interrupted_publish(self) -> None:
        if not require_regular_or_absent(self.publish_marker_path):
            return
        with self.store_lock():
            self._recover_interrupted_publish_locked()

    def _recover_interrupted_publish_locked(self) -> None:
        if not require_regular_or_absent(self.publish_marker_path):
            return
        if not require_regular_or_absent(self.tasks_path):
            raise TaskError(f"{self.publish_marker_path}: interrupted task publish cannot recover because tasks.jsonl is missing")
        items, _ = _parse_jsonl(self.tasks_path)
        _validate_items_for_write(items)
        staging = Path(tempfile.mkdtemp(prefix=".tasks-repair-", dir=str(self.docs_dir)))
        try:
            self._write_staged_generation(staging, items)
            _run_checker(staging)
            for target, source in {
                self.tasks_path: staging / "tasks.jsonl",
                self.data_js_path: staging / "tasks-data.js",
                self.task_decomposition_path: staging / "task-decomposition.md",
                self.tasks_done_path: staging / "tasks-done.md",
                self.bug_backlog_path: staging / "bug-backlog.md",
                self.bugs_done_path: staging / "bugs-done.md",
            }.items():
                source.replace(target)
            _fsync_dir(self.docs_dir)
            self._clear_publish_marker()
        finally:
            shutil.rmtree(staging, ignore_errors=True)

    def _snapshot_last_good(self, *, require_valid: bool = True) -> None:
        tasks_exists = require_regular_or_absent(self.tasks_path)
        data_exists = require_regular_or_absent(self.data_js_path)
        if not tasks_exists and not data_exists:
            return
        if not tasks_exists or not data_exists:
            if not require_valid and tasks_exists:
                return
            raise TaskError(f"{self.docs_dir}: missing tasks.jsonl or tasks-data.js; refusing mutation without a valid last-known-good pair")
        try:
            _run_checker(self.docs_dir)
        except TaskError:
            if require_valid:
                raise
            return
        self.log_dir.mkdir(parents=True, exist_ok=True)
        stamp = dt.datetime.now(dt.timezone.utc).strftime("%Y%m%dT%H%M%S%fZ")
        require_regular_file(self.tasks_path)
        require_regular_file(self.data_js_path)
        shutil.copy2(self.tasks_path, self.log_dir / f"tasks-{stamp}.jsonl")
        shutil.copy2(self.data_js_path, self.log_dir / f"tasks-data-{stamp}.js")
        self._prune_backups()

    def _prune_backups(self, keep: int = 20) -> None:
        for pattern in ("tasks-*.jsonl", "tasks-data-*.js"):
            backups = sorted(self.log_dir.glob(pattern), key=lambda p: p.stat().st_mtime, reverse=True)
            for path in backups[keep:]:
                with contextlib.suppress(OSError):
                    path.unlink()

    def project_ledger_records(self) -> list[dict[str, Any]]:
        if goalflight_ledger is None:
            return []
        target = _strip_managed_worktree(self.project_root)
        records = []
        for record in goalflight_ledger.read_records():
            if not _record_task_ids(record):
                continue
            raw_root = record.get("project_root")
            if not isinstance(raw_root, str) or not raw_root:
                continue
            if _strip_managed_worktree(Path(raw_root)) == target:
                records.append(record)
        return records

    def _records_by_task(self) -> dict[str, list[dict[str, Any]]]:
        records_by_task: dict[str, list[dict[str, Any]]] = {}
        for record in self.project_ledger_records():
            for task_id in _record_task_ids(record):
                records_by_task.setdefault(task_id, []).append(record)
        return records_by_task

    def sync_dispatch_breadcrumbs(self, items: list[dict[str, Any]], actor: str) -> int:
        by_id = {item["id"]: item for item in items}
        changed = 0
        for record in self.project_ledger_records():
            dispatch_id = record.get("dispatch_id")
            if not isinstance(dispatch_id, str):
                continue
            crumb = _breadcrumb_from_record(record)
            for task_id in _record_task_ids(record):
                item = by_id.get(task_id)
                if item is None:
                    continue
                dispatches = item.setdefault("dispatches", [])
                if not isinstance(dispatches, LIST_TYPE):
                    raise TaskError(f"item {task_id}: dispatches must be an array")
                existing_keys = {
                    _breadcrumb_key(entry)
                    for entry in dispatches
                    if isinstance(entry, dict)
                }
                if _breadcrumb_key(crumb) in existing_keys:
                    continue
                dispatches.append(crumb)
                changed += 1
                _append_audit(item, "dispatch-sync", actor, dispatch_id=dispatch_id, state=crumb.get("state"))
        return changed

    def append_dispatch_breadcrumbs(self, task_ids: list[str], breadcrumb: dict[str, Any], actor: str) -> int:
        clean_ids = []
        for task_id in task_ids:
            if isinstance(task_id, str) and task_id and task_id not in clean_ids:
                clean_ids.append(task_id)
        if not clean_ids:
            return 0
        dispatch_id = breadcrumb.get("dispatch_id")
        if not isinstance(dispatch_id, str) or not dispatch_id:
            raise TaskError("dispatch breadcrumb requires dispatch_id")
        state = breadcrumb.get("state")
        if not isinstance(state, str) or state not in TASK_DISPATCH_STATES:
            raise TaskError(f"dispatch breadcrumb requires state in {sorted(TASK_DISPATCH_STATES)}")
        crumb = {k: v for k, v in dict(breadcrumb).items() if v not in (None, "", [], {})}
        crumb.setdefault("ts", utc_now())

        def update(items: list[dict[str, Any]]) -> int:
            by_id = {item["id"]: item for item in items}
            changed = 0
            for task_id in clean_ids:
                item = by_id.get(task_id)
                if item is None:
                    raise TaskError(f"{self.tasks_path}: item not found: {task_id}")
                dispatches = item.setdefault("dispatches", [])
                if not isinstance(dispatches, LIST_TYPE):
                    raise TaskError(f"item {task_id}: dispatches must be an array")
                dispatches.append(dict(crumb))
                changed += 1
                _append_audit(item, "dispatch-breadcrumb", actor, dispatch_id=dispatch_id, state=state)
            return changed

        return self.mutate_items(update, allow_invalid_live_mirror=True)

    def derived_rows(self) -> list[dict[str, Any]]:
        return self.derived_rows_for_items(self.load_items())

    def derived_rows_for_items(self, items: list[dict[str, Any]]) -> list[dict[str, Any]]:
        records_by_task = self._records_by_task()
        by_id = {item["id"]: item for item in items}
        rows = []
        for item in items:
            item_id = item["id"]
            row = dict(item)
            row["derived_status"] = self._derive_status(item, records_by_task.get(item_id, []), by_id)
            row["query_epoch"] = _item_query_epoch(row)
            query_time = _iso_from_epoch(row["query_epoch"])
            if query_time:
                row["query_time"] = query_time
            rows.append(row)
        return rows

    def get_item(self, item_id: str) -> dict[str, Any]:
        for row in self.derived_rows():
            if row.get("id") == item_id:
                return row
        raise TaskError(f"{self.tasks_path}: item not found: {item_id}")

    def query_items(self, query: Any = None, **overrides: Any) -> list[dict[str, Any]]:
        q = _coerce_query(query, **overrides)
        rows = self.derived_rows()
        status = q.get("status")
        kind = q.get("kind")
        blockers = q.get("blocked_by") or []
        since_epoch = q.get("since_epoch")

        def matches(row: dict[str, Any]) -> bool:
            if status and not _matches_canned_status(row, status):
                return False
            if kind and row.get("kind", "task") != kind:
                return False
            if blockers:
                row_blockers = row.get("blocked_by")
                if not isinstance(row_blockers, LIST_TYPE) or any(blocker not in row_blockers for blocker in blockers):
                    return False
            filter_epoch = _item_dispatch_epoch(row) if status == "delegated" else int(row.get("query_epoch") or 0)
            if since_epoch is not None and filter_epoch < int(since_epoch):
                return False
            return True

        return [row for row in rows if matches(row)]

    def _derive_status(
        self,
        item: dict[str, Any],
        live_records: list[dict[str, Any]],
        by_id: dict[str, dict[str, Any]],
    ) -> str:
        if _is_done_reviewed(item):
            return "done-reviewed"
        candidates: list[tuple[dt.datetime, str]] = []
        if live_records:
            for record in live_records:
                status = _dispatch_status_from_record(record, live=True)
                if status:
                    candidates.append((_record_sort_time(record), status))
        crumb = _latest_dispatch_breadcrumb(item)
        if crumb:
            status = _dispatch_status_from_record(crumb, live=False)
            if status:
                candidates.append((_record_sort_time(crumb), status))
        if item.get("done") is True:
            candidates.append((_record_sort_time({"ts": item.get("done_at") or item.get("closed_at") or item.get("created_at")}), "awaiting-review"))
        if candidates:
            latest = sorted(candidates, key=lambda row: row[0])[-1][1]
            return "awaiting-review" if latest == "worker-finished" else latest
        if item.get("kind") == "decision":
            return "decision"
        blockers = item.get("blocked_by")
        if isinstance(blockers, LIST_TYPE) and blockers:
            unresolved = any(not _is_done_reviewed(by_id.get(str(blocker), {})) for blocker in blockers)
            if unresolved:
                return "waiting"
        return "pending"

    def generated_markdown(self, items: list[dict[str, Any]]) -> dict[str, str]:
        rows = self.derived_rows_for_items(items)
        lookup = _id_lookup(items)
        tasks = [row for row in rows if row.get("kind", "task") == "task"]
        bugs = [row for row in rows if row.get("kind") == "bug"]
        return {
            "task-decomposition.md": self._render_task_decomposition(tasks, lookup),
            "tasks-done.md": self._render_tasks_done(tasks, lookup),
            "bug-backlog.md": self._render_bug_backlog(bugs, lookup),
            "bugs-done.md": self._render_bugs_done(bugs, lookup),
        }

    def _render_header(self, title: str) -> list[str]:
        return [
            f"# {title}",
            "",
            "> Generated by `goalflight_task.py sync` from `tasks.jsonl` plus the project dispatch ledger. Do not edit by hand.",
            "> Section authority: `protocols/progress-dashboard.md`.",
            "",
        ]

    def _render_item_block(self, row: dict[str, Any], lookup: dict[str, str], *, done_view: bool = False) -> list[str]:
        item_id = str(row.get("id", ""))
        lines = [f"### {item_id}", "", f"**{_md_autolink_ids(row.get('title', '(untitled)'), lookup)}**", ""]
        if row.get("derived_status") and not done_view:
            lines.append(f"- Status: {_md_text(STATUS_LABELS.get(str(row['derived_status']), row['derived_status']))}")
        blockers = _md_list(row.get("blocked_by"), lookup)
        if blockers:
            lines.append(f"- Blocked by: {blockers}")
        links = _md_list(row.get("links"), lookup)
        if links:
            lines.append(f"- Links: {links}")
        for key, label in (
            ("lane", "Lane"),
            ("severity", "Severity"),
            ("pattern", "Pattern"),
            ("source", "Source"),
            ("acceptance", "Acceptance"),
            ("prompt_path", "Prompt"),
        ):
            value = row.get(key)
            if isinstance(value, str) and value:
                lines.append(f"- {label}: {_md_autolink_ids(value, lookup)}")
        if isinstance(row.get("prompt"), str) and row["prompt"]:
            lines.extend(["- Prompt:", ""])
            lines.extend(f"  {line}" if line else "" for line in _md_text(row["prompt"]).splitlines())
        lines.append("")
        return lines

    def _render_sections(
        self,
        rows: list[dict[str, Any]],
        lookup: dict[str, str],
        sections: tuple[tuple[str, str], ...],
        *,
        empty: str,
    ) -> list[str]:
        lines: list[str] = []
        any_rows = False
        for key, label in sections:
            lines.extend([f"## {label}", ""])
            section = _section_rows(rows, key)
            if section:
                any_rows = True
                for row in section:
                    lines.extend(self._render_item_block(row, lookup))
            else:
                lines.extend([empty, ""])
        if not any_rows and not rows:
            return [empty, ""]
        return lines

    def _render_lane_backlog(self, rows: list[dict[str, Any]], lookup: dict[str, str]) -> list[str]:
        backlog = [
            row
            for row in rows
            if row.get("derived_status") != "done-reviewed" and _is_reserved_lane(row)
        ]
        if not backlog:
            return []
        lines = ["## Backlog", ""]
        for row in backlog:
            lines.extend(self._render_item_block(row, lookup))
        return lines

    def _render_task_decomposition(self, tasks: list[dict[str, Any]], lookup: dict[str, str]) -> str:
        open_tasks = [
            row
            for row in tasks
            if row.get("derived_status") != "done-reviewed" and not _is_reserved_lane(row)
        ]
        lines = self._render_header("goal-flight — Task Decomposition")
        lines.extend(self._render_sections(open_tasks, lookup, MARKDOWN_SECTIONS, empty="_(none)_"))
        lines.extend(self._render_lane_backlog(tasks, lookup))
        return "\n".join(lines).rstrip() + "\n"

    def _render_tasks_done(self, tasks: list[dict[str, Any]], lookup: dict[str, str]) -> str:
        done = [row for row in tasks if row.get("derived_status") == "done-reviewed"]
        lines = self._render_header("goal-flight — Tasks Done")
        lines.extend(["## Done", ""])
        if done:
            for row in reversed(done):
                lines.extend(self._render_item_block(row, lookup, done_view=True))
        else:
            lines.extend(["_(none)_", ""])
        return "\n".join(lines).rstrip() + "\n"

    def _render_bug_backlog(self, bugs: list[dict[str, Any]], lookup: dict[str, str]) -> str:
        open_bugs = [
            row
            for row in bugs
            if row.get("derived_status") != "done-reviewed" and not _is_reserved_lane(row)
        ]
        lines = self._render_header("goal-flight — Bug Backlog")
        lines.extend(self._render_sections(open_bugs, lookup, MARKDOWN_SECTIONS, empty="_(none)_"))
        lines.extend(self._render_lane_backlog(bugs, lookup))
        return "\n".join(lines).rstrip() + "\n"

    def _render_bugs_done(self, bugs: list[dict[str, Any]], lookup: dict[str, str]) -> str:
        done = [row for row in bugs if row.get("derived_status") == "done-reviewed"]
        lines = self._render_header("goal-flight — Bugs Done")
        lines.extend(["## Done", ""])
        if done:
            for row in reversed(done):
                lines.extend(self._render_item_block(row, lookup, done_view=True))
        else:
            lines.extend(["_(none)_", ""])
        return "\n".join(lines).rstrip() + "\n"


def _kind_from_reference(value: str, default: str) -> str:
    lower = value.lower()
    if lower.startswith(("b-", "bp-")):
        return "bug"
    if lower.startswith(("q-", "adr-")):
        return "decision"
    if lower.startswith("t-"):
        return "task"
    return default


def _harvest_candidate(
    store: TaskStore,
    *,
    kind: str,
    title: str,
    source_path: Path,
    source_type: str,
    line: int | None = None,
    severity: str | None = None,
    pattern: str | None = None,
) -> dict[str, Any] | None:
    clean_title = _clean_markdown_title(title)
    if not clean_title or clean_title.lower() in {"none", "(none)"}:
        return None
    if _harvest_has_placeholder(clean_title):
        return None
    source_ref = _rel_source(store.project_root, source_path, line)
    source_file = _rel_source(store.project_root, source_path)
    tags = [*HARVEST_DRAFT_TAGS, source_type]
    if pattern:
        tags.append(pattern)
    candidate: dict[str, Any] = {
        "kind": kind,
        "title": clean_title,
        "links": [source_ref],
        "tags": tags,
        "source": "harvest",
        "source_ref": source_ref,
        "harvest_source": source_type,
        "harvest_key": f"harvest:{_short_hash([kind, _harvest_title_key(clean_title), source_file, source_type])}",
    }
    if severity:
        candidate["severity"] = severity
    if pattern:
        candidate["pattern"] = pattern
    return candidate


def _append_candidate(candidates: list[dict[str, Any]], candidate: dict[str, Any] | None) -> None:
    if candidate is None:
        return
    key = candidate.get("harvest_key")
    if key and any(existing.get("harvest_key") == key for existing in candidates):
        return
    title_key = (str(candidate.get("kind", "task")), _harvest_title_key(candidate.get("title")))
    if title_key[1] and any((str(existing.get("kind", "task")), _harvest_title_key(existing.get("title"))) == title_key for existing in candidates):
        return
    candidates.append(candidate)


def _block_is_open(block: dict[str, Any]) -> bool:
    status = _norm_key(block.get("status"))
    return status not in {"done", "done-reviewed", "closed", "fixed"}


def _harvest_heading_blocks(
    store: TaskStore,
    path: Path,
    *,
    default_kind: str,
    source_type: str,
) -> list[dict[str, Any]]:
    if not require_regular_or_absent(path):
        return []
    candidates: list[dict[str, Any]] = []
    current: dict[str, Any] | None = None

    def flush() -> None:
        nonlocal current
        if not current:
            return
        if not _block_is_open(current):
            current = None
            return
        title = current.get("title") or current.get("heading") or ""
        ref = str(current.get("ref") or "")
        _append_candidate(
            candidates,
            _harvest_candidate(
                store,
                kind=_kind_from_reference(ref, default_kind),
                title=str(title),
                source_path=path,
                source_type=source_type,
                line=int(current.get("line") or 0),
            ),
        )
        current = None

    text = path.read_text(encoding="utf-8", errors="replace")
    if _skip_harvest_source(path, text):
        return []
    lines = text.splitlines()
    for lineno, line in enumerate(lines, start=1):
        heading = re.match(r"^#{2,6}\s+((?:ADR|bp|[tbq])-\d+)\b(?:\s+(.+))?", line, re.IGNORECASE)
        if heading:
            flush()
            current = {
                "ref": heading.group(1),
                "heading": heading.group(2) or heading.group(1),
                "line": lineno,
            }
            continue
        if current is None:
            list_item = re.match(r"^\s*(?:[-*]\s+|\d+[.)]\s+)(?:\[[ xX]\]\s+)?(.+?)\s*$", line)
            if list_item and "Status:" not in list_item.group(1) and not _harvest_has_placeholder(list_item.group(1)):
                _append_candidate(
                    candidates,
                    _harvest_candidate(
                        store,
                        kind=default_kind,
                        title=list_item.group(1),
                        source_path=path,
                        source_type=source_type,
                        line=lineno,
                    ),
                )
            continue
        bold = re.search(r"\*\*(.+?)\*\*", line)
        if bold and not current.get("title"):
            current["title"] = bold.group(1)
        status = re.match(r"^\s*[-*]\s+Status:\s*(.+?)\s*$", line, re.IGNORECASE)
        if status:
            current["status"] = status.group(1)
    flush()
    return candidates


def _harvest_review_candidates(store: TaskStore) -> list[dict[str, Any]]:
    review_dir = store.docs_dir / "reviews"
    if not review_dir.is_dir():
        return []
    candidates: list[dict[str, Any]] = []
    for path in sorted(review_dir.rglob("*.md")):
        rel_lower = path.relative_to(review_dir).as_posix().lower()
        if "backlog" not in rel_lower and "finding" not in rel_lower:
            continue
        require_regular_file(path)
        text = path.read_text(encoding="utf-8", errors="replace")
        if _skip_harvest_source(path, text):
            continue
        lines = text.splitlines()
        for lineno, line in enumerate(lines, start=1):
            if _harvest_has_placeholder(line):
                continue
            bold = re.search(r"\*\*(.+?)\*\*", line)
            if not bold:
                continue
            text = _clean_markdown_title(bold.group(1))
            match = re.match(r"^(P[0-3]|critical|high|medium|low)\b\s*(?:[-:\u2014]\s*)?(.*)$", text, re.IGNORECASE)
            if not match:
                continue
            severity_token = match.group(1).upper()
            title = match.group(2).strip() or text
            severity = REVIEW_SEVERITY.get(severity_token, severity_token.lower())
            _append_candidate(
                candidates,
                _harvest_candidate(
                    store,
                    kind="bug",
                    title=title,
                    source_path=path,
                    source_type="review-finding",
                    line=lineno,
                    severity=severity,
                ),
            )
    return candidates


def _resume_sort_key(path: Path) -> tuple[str, int, str]:
    match = RESUME_NOTES_RE.match(path.name)
    if not match:
        return ("0000-00-00", 0, path.name)
    return (match.group(1), int(match.group(2) or 0), path.name)


def _newest_resume_note(docs_dir: Path) -> Path | None:
    notes = sorted(docs_dir.glob("RESUME-NOTES-*.md"), key=_resume_sort_key)
    return notes[-1] if notes else None


def _harvest_resume_candidates(store: TaskStore) -> list[dict[str, Any]]:
    path = _newest_resume_note(store.docs_dir)
    if path is None:
        return []
    require_regular_file(path)
    text = path.read_text(encoding="utf-8", errors="replace")
    if _skip_harvest_source(path, text):
        return []
    candidates: list[dict[str, Any]] = []
    section: str | None = None
    for lineno, line in enumerate(text.splitlines(), start=1):
        heading = re.match(r"^#{2,6}\s+(.+?)\s*$", line)
        if heading:
            title = _norm_key(heading.group(1))
            if any(key in title for key in ("next action", "current goal", "remaining")):
                section = "task"
            elif "do not re-litigate" in title:
                section = "decision"
            else:
                section = None
            continue
        if section is None:
            continue
        list_item = re.match(r"^\s*(?:[-*]\s+|\d+[.)]\s+)(?:\[[ xX]\]\s+)?(.+?)\s*$", line)
        if not list_item:
            continue
        text = list_item.group(1)
        if _harvest_has_placeholder(text):
            continue
        kind = "decision" if section == "decision" else "task"
        _append_candidate(
            candidates,
            _harvest_candidate(
                store,
                kind=kind,
                title=text,
                source_path=path,
                source_type=f"resume-{section}",
                line=lineno,
            ),
        )
    return candidates


def _collect_harvest_candidates(store: TaskStore) -> list[dict[str, Any]]:
    candidates: list[dict[str, Any]] = []
    bug_paths = [
        store.docs_dir / "bug-backlog.md",
        *store.docs_dir.glob("bug-pattern*.md"),
        *store.docs_dir.glob("bug-patterns/*.md"),
    ]
    for path in sorted(bug_paths):
        candidates.extend(_harvest_heading_blocks(store, path, default_kind="bug", source_type="bug-backlog"))
    candidates.extend(_harvest_review_candidates(store))
    candidates.extend(_harvest_resume_candidates(store))
    deduped: list[dict[str, Any]] = []
    for candidate in candidates:
        _append_candidate(deduped, candidate)
    return deduped


def _existing_harvest_keys(items: list[dict[str, Any]]) -> tuple[set[str], set[tuple[str, str]]]:
    keys: set[str] = set()
    titles: set[tuple[str, str]] = set()
    for item in items:
        key = item.get("harvest_key")
        if isinstance(key, str) and key:
            keys.add(key)
        kind = str(item.get("kind", "task"))
        title = _harvest_title_key(item.get("title"))
        if title:
            titles.add((kind, title))
    return keys, titles


def _add_harvest_candidates(store: TaskStore, items: list[dict[str, Any]], actor: str, candidates: list[dict[str, Any]]) -> dict[str, Any]:
    existing_keys, existing_titles = _existing_harvest_keys(items)
    created: list[str] = []
    skipped = 0
    for candidate in candidates:
        key = str(candidate.get("harvest_key") or "")
        title_key = (str(candidate.get("kind", "task")), _harvest_title_key(candidate.get("title")))
        if (key and key in existing_keys) or title_key in existing_titles:
            skipped += 1
            continue
        item_id = store.reserve_id(FAMILY_PREFIX_BY_KIND[str(candidate["kind"])])
        item = _make_item(
            item_id,
            kind=str(candidate["kind"]),
            title=str(candidate["title"]),
            actor=actor,
            links=LIST_TYPE(candidate.get("links") or []),
            tags=LIST_TYPE(candidate.get("tags") or HARVEST_DRAFT_TAGS),
        )
        for field in ("source", "source_ref", "harvest_source", "harvest_key", "severity", "pattern"):
            value = candidate.get(field)
            if value not in (None, "", [], {}):
                item[field] = value
        _append_audit(item, "harvest", actor, source_ref=item.get("source_ref"), harvest_source=item.get("harvest_source"))
        items.append(item)
        created.append(item_id)
        if key:
            existing_keys.add(key)
        existing_titles.add(title_key)
    return {"created": created, "skipped": skipped, "candidates": len(candidates)}


def _first_resume_paragraph(lines: list[str], heading: str) -> str:
    active = False
    buf: list[str] = []
    for line in lines:
        header = re.match(r"^#{2,6}\s+(.+?)\s*$", line)
        if header:
            if active and buf:
                break
            active = _norm_key(header.group(1)) == _norm_key(heading)
            continue
        if not active:
            continue
        if not line.strip():
            if buf:
                break
            continue
        if line.lstrip().startswith(("-", "*")):
            continue
        buf.append(_clean_markdown_title(line))
    return " ".join(buf).strip()


def _first_resume_next(lines: list[str]) -> str:
    active = False
    for line in lines:
        header = re.match(r"^#{2,6}\s+(.+?)\s*$", line)
        if header:
            active = "next action" in _norm_key(header.group(1))
            continue
        if not active:
            continue
        item = re.match(r"^\s*(?:[-*]\s+|\d+[.)]\s+)(?:\[[ xX]\]\s+)?(.+?)\s*$", line)
        if item:
            return _clean_markdown_title(item.group(1))
    return ""


def _append_resume_history(store: TaskStore, actor: str) -> list[str]:
    notes = sorted(store.docs_dir.glob("RESUME-NOTES-*.md"), key=_resume_sort_key)
    if not notes:
        return []
    history = store.docs_dir / "history.md"
    existing = history.read_text(encoding="utf-8") if require_regular_or_absent(history) else (
        "# goal-flight - History (write-once)\n\n"
        "> Append-only. One entry per compaction handoff. Never edit prior entries.\n"
        "> Living state: newest `RESUME-NOTES-*.md`.\n"
    )
    by_date: dict[str, list[Path]] = {}
    for path in notes:
        date, _, _ = _resume_sort_key(path)
        by_date.setdefault(date, []).append(path)
    additions: list[tuple[str, list[str]]] = []
    for path in notes:
        require_regular_file(path)
        rel = _rel_source(store.project_root, path)
        if _history_has_source(existing, rel) or any(rel == entry_rel for entry_rel, _ in additions):
            continue
        date, rev, _ = _resume_sort_key(path)
        lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
        summary = _first_resume_paragraph(lines, "TL;DR") or _clean_markdown_title(lines[0] if lines else path.stem)
        next_action = _first_resume_next(lines)
        superseded = [
            _rel_source(store.project_root, old)
            for old in by_date.get(date, [])
            if old != path and _resume_sort_key(old)[1] < rev
        ]
        entry = ["", f"## {date} - {path.name}", "", f"- Source: {rel}"]
        if summary:
            entry.append(f"- Summary: {summary[:280]}")
        if next_action:
            entry.append(f"- Next: {next_action[:220]}")
        if superseded:
            entry.append(f"- Supersedes: {', '.join(superseded)}")
        entry.append(f"- Harvested by: {actor}")
        additions.append((rel, entry))
    appended: list[str] = []
    if additions:
        with store.store_lock():
            current = history.read_text(encoding="utf-8") if require_regular_or_absent(history) else existing
            missing = [(rel, entry) for rel, entry in additions if not _history_has_source(current, rel)]
            if missing:
                _append_history_entries(history, current, missing)
                appended = [rel for rel, _ in missing]
    return appended


def _review_bug_key(item_id: str, dispatch_id: str, title: str) -> str:
    return f"review-bug:{_short_hash([item_id, dispatch_id, _norm_key(title)])}"


def _split_csv(values: list[str] | None) -> list[str]:
    out: list[str] = []
    for value in values or []:
        for part in value.split(","):
            part = part.strip()
            if part:
                out.append(part)
    return out


def _create_item(store: TaskStore, args: argparse.Namespace, actor: str) -> str:
    """Reserve an id and append a new item from args. Shared by `new` and `capture`."""
    family = args.id_family or FAMILY_PREFIX_BY_KIND[args.kind]
    item_id = store.reserve_id(family)

    def update(items: list[dict[str, Any]]) -> str:
        if any(item.get("id") == item_id for item in items):
            raise TaskError(f"{store.tasks_path}: id {item_id} already exists; .task-seq is stale")
        item = _make_item(
            item_id,
            kind=args.kind,
            title=args.title,
            actor=actor,
            blocked_by=_split_csv(args.blocked_by),
            links=_split_csv(args.links),
            tags=_split_csv(args.tags),
            lane=args.lane,
        )
        for key in ("acceptance", "prompt", "prompt_path", "pattern", "severity", "source"):
            value = getattr(args, key, None)
            if value not in (None, "", [], {}):
                item[key] = value
        items.append(item)
        return item_id

    return store.mutate_items(update)


def _cmd_new(store: TaskStore, args: argparse.Namespace) -> int:
    created = _create_item(store, args, _actor(args))
    if args.json:
        print(json.dumps({"id": created}, sort_keys=True))
    else:
        print(created)
    return 0


def _cmd_capture(store: TaskStore, args: argparse.Namespace) -> int:
    # Reflex defaults for the zero-flag common path (`capture "<what>"`): a task
    # parked in the deferred backlog, sourced to the cwd. --severity implies a bug.
    if getattr(args, "kind", None) is None:
        args.kind = "bug" if getattr(args, "severity", None) else "task"
    if not getattr(args, "lane", None):
        args.lane = "deferred"
    if not getattr(args, "source", None):
        args.source = os.getcwd()
    created = _create_item(store, args, _actor(args))
    if args.json:
        print(json.dumps({"id": created}, sort_keys=True))
    else:
        print(created)
    # One next-step hint to STDERR (keeps stdout / `--json | jq` clean; never an interrupt).
    print(
        f"captured {created} ({args.lane}). promote: goalflight_task.py lane {created} <name>",
        file=sys.stderr,
    )
    return 0


def _cmd_show(store: TaskStore, args: argparse.Namespace) -> int:
    item = store.get_item(args.item_id)
    if args.prompt:
        prompt = item.get("prompt")
        if isinstance(prompt, str):
            print(prompt)
            return 0
        prompt_path = item.get("prompt_path")
        if isinstance(prompt_path, str) and prompt_path:
            path = Path(prompt_path)
            if not path.is_absolute():
                path = store.project_root / path
            require_regular_file(path)
            print(path.read_text(encoding="utf-8"), end="")
            return 0
        raise TaskError(f"{args.item_id}: no prompt or prompt_path")
    if args.json:
        print(json.dumps(item, ensure_ascii=False, sort_keys=True))
    else:
        print(json.dumps(item, indent=2, ensure_ascii=False, sort_keys=True))
    return 0


def _cmd_block(store: TaskStore, args: argparse.Namespace) -> int:
    blockers = _split_csv(args.on)
    if not blockers:
        raise TaskError("block: --on requires at least one blocker id")
    actor = _actor(args)

    def update(items: list[dict[str, Any]]) -> None:
        by_id = {item["id"]: item for item in items}
        item = by_id.get(args.item_id)
        if item is None:
            raise TaskError(f"{store.tasks_path}: item not found: {args.item_id}")
        blocked_by = item.setdefault("blocked_by", [])
        if not isinstance(blocked_by, LIST_TYPE):
            raise TaskError(f"item {args.item_id}: blocked_by must be an array")
        for blocker in blockers:
            if blocker not in blocked_by:
                blocked_by.append(blocker)
        _append_audit(item, "block", actor, on=blockers)

    store.mutate_items(update)
    print(args.item_id)
    return 0


def _cmd_unblock(store: TaskStore, args: argparse.Namespace) -> int:
    actor = _actor(args)
    remove = set(_split_csv(args.on))

    def update(items: list[dict[str, Any]]) -> None:
        by_id = {item["id"]: item for item in items}
        item = by_id.get(args.item_id)
        if item is None:
            raise TaskError(f"{store.tasks_path}: item not found: {args.item_id}")
        blocked_by = item.setdefault("blocked_by", [])
        if not isinstance(blocked_by, LIST_TYPE):
            raise TaskError(f"item {args.item_id}: blocked_by must be an array")
        item["blocked_by"] = [value for value in blocked_by if remove and value not in remove] if remove else []
        _append_audit(item, "unblock", actor, on=sorted(remove) if remove else None)

    store.mutate_items(update)
    print(args.item_id)
    return 0


