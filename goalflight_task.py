def _audit(action: str, actor: str, **extra: Any) -> dict[str, Any]:
    entry: dict[str, Any] = {"at": utc_now(), "actor": actor, "action": action}
    entry.update({k: v for k, v in extra.items() if v not in (None, [], {})})
    return entry


def _append_audit(item: dict[str, Any], action: str, actor: str, **extra: Any) -> None:
    audit = item.setdefault("audit", [])
    if not isinstance(audit, LIST_TYPE):
        raise TaskError(f"item {item.get('id', '<unknown>')}: audit must be an array")
    audit.append(_audit(action, actor, **extra))


def _make_item(
    item_id: str,
    *,
    kind: str,
    title: str,
    actor: str,
    blocked_by: list[Any] | tuple[Any, ...] | None = None,
    links: list[Any] | tuple[Any, ...] | None = None,
    tags: list[Any] | tuple[Any, ...] | None = None,
    lane: str | None = None,
    audit_action: str = "new",
) -> dict[str, Any]:
    item = {
        "schema_version": ITEM_SCHEMA_VERSION,
        "id": item_id,
        "kind": kind,
        "title": title,
        "blocked_by": _dedupe_strs(blocked_by),
        "links": _dedupe_strs(links),
        "done": False,
        "tags": _dedupe_strs(tags),
        "created_at": utc_now(),
        "created_by": actor,
        "audit": [_audit(audit_action, actor)],
    }
    if lane is not None:
        item["lane"] = lane
    return item


